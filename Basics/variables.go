package basics

import "fmt"

func Variables() {
	// Variable declaration
	var name string = "John"
	var age int = 30
	var height float64 = 5.9

	// Shorthand variable declaration
	students :="Students"

	// Print the array
	fmt.Println("Students:", students)

	// Print the variables
	fmt.Println("Name:", name)
	fmt.Println("Age:", age)
	fmt.Println("Height:", height)
	
}